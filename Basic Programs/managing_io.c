#include<stdio.h>

void main()
{
    int n;
    float f;
    double d;
    char c;
    char str[100]; // String
    

    // Taking Input scanf()
    printf("Enter Number n:");
    scanf("%d",&n);

    printf("Enter Number f:");
    scanf("%f",&f);

    printf("Enter Character C :");
    scanf("%c",&c);

    printf("Enter Double d :");
    scanf("%lf",&d);

    printf("Enter String str:");
    scanf("%s",&str);

    
}